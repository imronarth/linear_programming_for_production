    private void inputbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputbtnActionPerformed
        // INPUT DATA
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        try{
            sql="insert into penjualan(`Kode Penjualan`,`Periode`,`Tahun`,`Actual Sales`,`Kode Produk`)"
                    + "values('"+kpenjualantxt.getText()+"'"
                    + ",'"+Integer.parseInt(periodetxt.getText())+"'"
                    + ",'"+Integer.parseInt(tahuntxt.getText())+"'"
                    + ",'"+Integer.parseInt(actualsalestxt.getText())+"'"
                    + ",'"+jComboBox1.getSelectedItem()+"')";
            pse=koneksi.konek().prepareStatement(sql);
            int masuk = pse.executeUpdate();
            model.setRowCount(0);
            tampil();
            if(masuk==1){
                JOptionPane.showMessageDialog(rootPane, "DATA BERHASIL DIINPUT");
            }
            
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "DATA GAGAL DIINPUT MOHON DATA DIPERIKSA KEMBALI");
        }
    }//GEN-LAST:event_inputbtnActionPerformed

    private void editbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editbtnActionPerformed
        // EDIT DATA
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        try{
            sql="update penjualan set"
                    + "`Periode` = '"+Integer.parseInt(periodetxt.getText())+"'"
                    + ",`Tahun` = '"+Integer.parseInt(tahuntxt.getText())+"'"
                    + ",`Actual Sales` = '"+Integer.parseInt(actualsalestxt.getText())+"'"
                    + ",`Kode Produk` = '"+jComboBox1.getSelectedItem()+"'"
                    + "where `Kode Penjualan` ='"+kpenjualantxt.getText()+"'";
            pse=koneksi.konek().prepareCall(sql);
            int edit = pse.executeUpdate();
            model.setRowCount(0);
            tampil();
            if(edit==1){
                JOptionPane.showMessageDialog(rootPane, "DATA BERHASIL DIUBAH");
            }
        }catch (Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "DATA GAGAL DIUBAH MOHON PERIKSA KEMBALI DATA INPUTAN");
        }
    }//GEN-LAST:event_editbtnActionPerformed

