    public void tampil(){
        DefaultTableModel model2=(DefaultTableModel) jTable1.getModel();
        try{
            String sql2="select * from penjualan";
            pse=koneksi.konek().prepareStatement(sql2);
            rse=pse.executeQuery();
            
            while(rse.next()){
                String column1=rse.getString("Kode Penjualan");
                String column2=Integer.toString(rse.getInt("Periode"));
                String column3=Integer.toString(rse.getInt("Tahun"));
                String column4=Integer.toString(rse.getInt("Actual Sales"));
                String column5=rse.getString("Kode Produk");
                
                model2.addRow(new Object[]{column1,column2,column3,column4,column5});
            }
        }catch (Exception e){
            e.printStackTrace();
        }
    }
